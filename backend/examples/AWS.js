// Практические советы по работе с AWS:
// 1. Не работать под root
// Root-аккаунт AWS предназначен только для начальной настройки.
// Под root у вас безграничные права: можно удалить все ресурсы, закрыть аккаунт, потерять данные.
// Рекомендуется:
// Зайти под root только один раз, включить MFA (двухфакторную аутентификацию).
// Создать отдельного IAM-пользователя (или IAM-группу) с правами AdministratorAccess.
// Работать всегда только через IAM-пользователей.
// 2. Создайте IAM-пользователя/группу
// В AWS Console -> IAM -> Users -> Add user.
// Дайте права через группу Administrators или прикрепите политику AdministratorAccess.
// Используйте Access keys только для CLI/SDK, и никогда не коммитьте их в git.
// Для командной работы создавайте отдельного пользователя на каждого разработчика (не делитесь root-данными).
// 3. Настройте безопасность с самого начала
// Включите MFA (2FA) на root и IAM-аккаунты.
// Настройте CloudTrail -> аудит действий (чтобы видеть, кто что делал).
// Включите AWS Config -> слежение за изменениями конфигурации.
// Настройте Billing alarms (например, предупреждение, если счёт > 10$).
// 5. Организация проекта
// Используйте S3 для хранения статики (картинок, логов, бэкапов).
// Для деплоя бэкенда выбирай по простоте:
// EC2 (виртуалка, всё ставите сами).
// ECS (Fargate) или EKS (контейнеры).
// Elastic Beanstalk (самый быстрый старт - деплой кода в контейнер).
// Под базы данных лучше сразу взять RDS (Postgres/MySQL) или DynamoDB, чем держать базу на EC2.
// 6. Инфраструктура как код (по возможности)
// Используйте Terraform или AWS CDK, чтобы всё было в коде.
// Так будет легко пересоздавать окружение (dev/staging/prod).
// 7. Практические советы
// Делайте отдельные окружения (dev/staging/prod), не работайте только в одном бакете и одной базе.
// Настройте S3 bucket policies: если файлы должны быть публичными — public-read, если приватными — только через pre-signed URL.
// Мониторинг: включите CloudWatch Alarms (CPU, RAM, Billing).
// Минимальный чеклист для первого проекта:
// Войти под root -> включить MFA -> создать IAM admin-пользователя.
// Работать только под IAM-пользователем.
// Настроить CloudTrail, Billing Alerts.
// Хранить секреты в AWS Secrets Manager или хотя бы в .env (не в git!).
